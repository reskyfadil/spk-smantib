    <h2 style="text-align: center;"><strong>Sistem Pendukung Keputusan</strong></h2>
                <p style="text-align: justify;"> Metode AHP (Analytic Hierarchy Process) adalah suatu metode pengambilan keputusan yang digunakan untuk menentukan prioritas dan bobot relatif dari beberapa kriteria atau alternatif. Metode Analytic Hierarchy Process (AHP) adalah teknik pengambilan keputusan yang dikembangkan oleh Thomas L. Saaty pada tahun 1970-an. AHP digunakan untuk memecahkan masalah yang kompleks dengan memecahnya menjadi bagian-bagian yang lebih sederhana. Proses ini melibatkan penentuan kriteria, subkriteria, dan alternatif, kemudian melakukan perbandingan berpasangan untuk menilai kepentingan relatif dari setiap elemen. Hasil dari perbandingan ini kemudian diolah untuk menghasilkan bobot numerik yang mencerminkan prioritas setiap elemen, membantu pengambil keputusan dalam memilih alternatif terbaik berdasarkan bobot tersebut.</p>
                <p style="text-align: justify;"> AHP digunakan dalam sistem pendukung keputusan karena kemampuannya untuk menangani keputusan yang melibatkan banyak kriteria dan subkriteria. Metode ini memudahkan pengambil keputusan dalam menguraikan masalah yang kompleks dan memberikan struktur yang sistematis untuk mengevaluasi berbagai pilihan. Dengan menggunakan AHP, pengambil keputusan dapat membuat penilaian yang lebih objektif dan transparan, serta memastikan bahwa semua faktor penting dipertimbangkan secara konsisten.</p>
                <p style="text-align: justify;"> Kelebihan AHP meliputi kemampuannya untuk menyederhanakan masalah kompleks dan memberikan kerangka kerja yang jelas untuk mengevaluasi pilihan. AHP juga memungkinkan penggabungan data kualitatif dan kuantitatif serta memberikan justifikasi yang kuat untuk keputusan yang diambil. Namun, kekurangannya termasuk ketergantungan pada penilaian subjektif yang bisa bias jika tidak dilakukan dengan hati-hati, serta proses perhitungan yang bisa menjadi sangat rumit dan memakan waktu jika melibatkan banyak kriteria dan alternatif. Meskipun demikian, dengan perangkat lunak yang mendukung, AHP tetap menjadi alat yang sangat berguna dalam pengambilan keputusan yang kompleks.</p>       
